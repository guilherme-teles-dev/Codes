from math import gcd
answers = int(input())
for cases in range(answers):
    pair1 = int(input(), 2)
    pair2 = int(input(), 2)
    loveispossible = False
    if(gcd(pair1, pair2) > 1):
        loveispossible = True
    print(f"Pair #{cases+1}: ", end='')
    if(loveispossible):
        print("All you need is love!")
    else:
        print("Love is not all you need!")
